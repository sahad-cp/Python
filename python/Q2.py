'''2 . Check if the first and last number of a list is the same
	lists : 
		X = [10,20,30,40,50,10]
		Y = [75,65,35,75,33.50]'''
  
x = [10,20,30,40,50,10]
y = [75,65,35,75,33.50]

if x[0] == x[-1]:
    print('1. the number is same')
else:
    print('1. the number is not same')  
    

if y[0] == y[-1]:
    print('2. the number is same')
else:
    print('2. the number is not same') 